# --- 2. Hyperparameters ---
batch_size = 64  # How many independent sequences will we process in parallel?
block_size = 256  # What is the maximum context length for predictions?
max_iters = 5000  # How many training steps
eval_interval = 500  # How often to check loss
learning_rate = 3e-4  # The "step size" for the optimizer
device = 'cuda' if torch.cuda.is_available() else 'cpu'  # Use GPU if available
eval_iters = 200
n_embd = 384  # Size of the vector for each token
n_head = 6  # How many self-attention heads
n_layer = 6  # How many transformer blocks
dropout = 0.2  # Randomly ignore 20% of neurons to prevent overfitting
